interface LoadingSpinnerProps {
  size?: 'sm' | 'md' | 'lg';
  className?: string;
}

export function LoadingSpinner({ size = 'md', className = '' }: LoadingSpinnerProps) {
  const sizeClasses = {
    sm: 'w-4 h-4',
    md: 'w-8 h-8',
    lg: 'w-12 h-12'
  };

  return (
    <div className={`${sizeClasses[size]} mx-auto border-4 border-blue-200 border-t-blue-500 rounded-full animate-spin ${className}`}>
      <span className="sr-only">Loading...</span>
    </div>
  );
}